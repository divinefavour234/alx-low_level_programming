#include "main.h"

/**
 * mul - Declaration of Function mul
 *
 * Description: Multiply two numbers together
 *
 * @a: Accept parameter which should be an integer
 *
 * @b: Accept parameter which should be an integer
 *
 * Return: return (c) which will be the result of the multiplication
 */
int mul(int a, int b)
{
	int c;

	c = a * b;

	return (c);
}
